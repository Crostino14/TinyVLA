    raw_mapping["Pull the top layer of the drawer and put the bowl inside"] = orig_4   # L1 alt: pull only
    raw_mapping["The top layer of the drawer needs to be opened and the bowl needs to be put inside"] = orig_4  # L2: passive
    raw_mapping["Open the top layer of the drawer and put the object between the plate and the cream cheese inside"] = orig_4  # L3: indirect

    # ── Task 5: Put the bowl on the top of the drawer ─────────────────────
    orig_5 = "Put the bowl on the top of the drawer"
    raw_mapping["Put the bowl on top of the drawer"] = orig_5              # DEFAULT (no "the")
    raw_mapping["put the bowl on the top of the cabinet"] = orig_5         # Alternate default (cabinet)
    raw_mapping["Place the bowl on the top of the cabinet"] = orig_5       # L1: verb swap
    raw_mapping["The top of the cabinet needs to have the bowl on it"] = orig_5  # L2: passive
    raw_mapping["Put the object between the wine bottle and the cream cheese on the top of the cabinet"] = orig_5  # L3: indirect

