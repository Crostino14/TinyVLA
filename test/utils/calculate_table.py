def find_txt_files_by_seed(base_dir, pattern_prefix="EVAL-libero_goal-tiny_vla"):
    """
    Automatically discover TinyVLA evaluation log files organized by seed index.

    Searches `base_dir` for `.txt` files whose names match the pattern:
        {pattern_prefix}*seed{0,1,2}*.txt

    This naming convention is generated automatically by the TinyVLA evaluation
    scripts, which embed the seed index in the filename. For example:
        EVAL-libero_goal-tiny_vla-2025-06-01_12-00-00--seed0--tasks1-5.txt

    Parameters
    ----------
    base_dir : str
        Directory path in which to search for log files.
    pattern_prefix : str, optional
        Filename prefix shared by all log files to search for.
        Default: "EVAL-libero_goal-tiny_vla".

    Returns
    -------
    dict or None
        Dictionary mapping seed index (int: 0, 1, 2) → sorted list of matching
        file path strings. Returns None if no files are found for any seed,
        allowing the caller to handle the error.

    Notes
    -----
    - Files within each seed are sorted alphabetically by filename, which
      typically corresponds to the task group order (e.g., tasks 1-5 before 6-10).
    - Both underscore (`_`) and hyphen (`-`) separators are supported because
      `glob` treats them as literal characters in the `*seed{N}*` wildcard.
    - If any seed has zero matching files, a warning is printed and None is
      returned immediately, preventing a partially-initialized result.

    Example
    -------
    >>> files = find_txt_files_by_seed("/logs", "EVAL-libero_goal-tiny_vla")
    >>> files[0]
    ['/logs/EVAL-libero_goal-tiny_vla-2025-06-01--seed0--part1.txt',
     '/logs/EVAL-libero_goal-tiny_vla-2025-06-01--seed0--part2.txt']
    """
    txt_files_by_seed = {0: [], 1: [], 2: []}  # Initialize empty lists for each seed

    print(f"\n[INFO] Cercando file .txt in: {base_dir}")
    print(f"Pattern: {pattern_prefix}*seed*{{0,1,2}}*.txt")

    for seed_idx in range(3):
        # Build the glob pattern: matches any file with the prefix and "seed{N}" in the name
        pattern = os.path.join(base_dir, f"{pattern_prefix}*seed{seed_idx}*.txt")
        matches = glob.glob(pattern)  # Expand the pattern to a list of matching paths

        matches.sort()  # Sort alphabetically to ensure consistent processing order

        txt_files_by_seed[seed_idx] = matches

        if matches:
            print(f"  ✓ Seed {seed_idx}: {len(matches)} file")
            for m in matches:
                print(f"    • {os.path.basename(m)}")  # Show filename only (not full path)
        else:
            # Missing files for this seed: the caller must handle this error
            print(f"  ✗ Seed {seed_idx}: nessun file trovato!")
            return None  # Return None to signal that the auto-detect failed

    return txt_files_by_seed


