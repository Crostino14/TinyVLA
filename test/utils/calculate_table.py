def parse_txt_file(filepath):
    """
    Parse a single TinyVLA evaluation log file and extract per-task success rates.

    The expected log file format uses the following marker lines:
      - "Original Command: <task_description>"
        or "Variation Command: <task_description>"
          → marks the start of a task block and sets `current_task`
      - "# episodes: <N>"
          → records the total number of episodes run for this task
      - "Task success rate: <X.XXXX>"
          → records the decimal success rate; converted to percentage

    Each task block is delimited by the "Task success rate:" line, which also
    resets the parser state (`current_task` and `current_episodes`).

    Parameters
    ----------
    filepath : str
        Absolute or relative path to the `.txt` log file to parse.

    Returns
    -------
    rates : dict
        Mapping from task_description (str) → success rate in percent (float).
        Example: {"Put the bowl on the stove": 84.0}
    episodes : dict
        Mapping from task_description (str) → number of episodes run (int).
        Defaults to 50 if the "# episodes:" line was not found.
    task_order : list of str
        Task description strings in the order they first appear in the file.
        Used to preserve log file ordering when merging across files.

    Notes
    -----
    - Duplicate task entries (same `current_task` appearing more than once)
      are silently ignored; only the first occurrence is retained. This
      prevents double-counting when logs contain repeated summary blocks.
    - The success rate is read as a decimal (e.g., 0.8400) and multiplied
      by 100 to convert to a percentage (84.0%) for display.
    - Regex patterns are used for robust extraction of numeric values even
      if surrounding text varies slightly between log versions.
    """
    rates = {}         # task → success rate (%)
    episodes = {}      # task → episode count
    task_order = []    # ordered list of task descriptions encountered

    with open(filepath, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    current_task = None      # Tracks the active task block being parsed
    current_episodes = None  # Tracks the episode count for the current task block

    for i, line in enumerate(lines):
        line = line.strip()  # Remove leading/trailing whitespace and newline characters

        # ── Detect task command markers ────────────────────────────────────
        # "Original Command:" marks the start of a default/training-distribution task block
        if "Original Command:" in line:
            current_task = line.split("Original Command:")[-1].strip()
        # "Variation Command:" marks the start of a linguistic variation task block
        elif "Variation Command:" in line:
            current_task = line.split("Variation Command:")[-1].strip()

        # ── Extract episode count ──────────────────────────────────────────
        # Match "# episodes: N" to record how many trials were run for this task
        if current_task and "# episodes:" in line:
            match = re.search(r'# episodes:\s*(\d+)', line)
            if match:
                current_episodes = int(match.group(1))  # Convert captured string to integer

        # ── Extract success rate and finalize task block ───────────────────
        # "Task success rate: X.XXXX" marks the end of a task block
        if current_task and "Task success rate:" in line:
            match = re.search(r'Task success rate:\s*([\d.]+)', line)
            if match:
                # Convert decimal rate to percentage (e.g., 0.8400 → 84.0)
                rate_value = float(match.group(1)) * 100

                if current_task not in rates:  # Skip duplicates (keep first occurrence only)
                    rates[current_task] = rate_value
                    # Use parsed episode count, or fall back to 50 (standard LIBERO default)
                    episodes[current_task] = current_episodes if current_episodes else 50
                    task_order.append(current_task)

                # Reset parser state for the next task block
                current_task = None
                current_episodes = None

    return rates, episodes, task_order


def merge_txt_files(txt_files_list):
    """
    Merge multiple TinyVLA log files belonging to the same random seed.

    When a single evaluation run is split across multiple log files (e.g.,
    different task groups run in separate jobs), this function aggregates
    all results. If the same task appears in multiple files, its success rates
    are averaged (arithmetic mean).

    Parameters
    ----------
    txt_files_list : list of str
        List of file paths to `.txt` log files for a single seed. May contain
        one or more files.

    Returns
    -------
    merged_rates : dict
        Mapping from task_description (str) → averaged success rate in % (float).
        If a task appears in N files, the reported rate is the mean of N values.
    task_episodes : dict
        Mapping from task_description (str) → episode count (int).
        Always set to 50, since LIBERO evaluations use 50 trials per task.
    all_tasks : list of str
        Task descriptions in the order they were first encountered across
        all merged files. Preserves cross-file ordering.

    Notes
    -----
    - The defaultdict(list) allows appending multiple success rate values
      for the same task without initialization.
    - Episode count is hardcoded to 50 per task, matching the LIBERO standard.
      This is intentional: averaging partial episode counts would be misleading.
    """
    from collections import defaultdict

    # Maps task_description → list of success rate values (one per file where it appears)
    task_rates_list = defaultdict(list)
    task_episodes = {}    # task → episode count (always 50)
    all_tasks = []        # Ordered list of unique task descriptions seen across all files

    for filepath in txt_files_list:
        # Parse each file and accumulate results
        rates, episodes, task_order = parse_txt_file(filepath)

        for task in task_order:
            if task not in task_rates_list:
                all_tasks.append(task)  # First time seeing this task: add to ordered list
            task_rates_list[task].append(rates[task])  # Append this file's rate to the list
            task_episodes[task] = 50  # Hardcode episode count to LIBERO standard (50 per task)

    # Compute per-task arithmetic mean across all files that reported it
    merged_rates = {}
    for task, rates_list in task_rates_list.items():
        merged_rates[task] = sum(rates_list) / len(rates_list)  # Arithmetic mean

    return merged_rates, task_episodes, all_tasks


