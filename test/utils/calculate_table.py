    raw_mapping["Put the object between the wine bottle and the cream cheese on the stove"] = orig_2  # L3: indirect reference

    # ── Task 3: Put the wine bottle on the top of the drawer ─────────────
    orig_3 = "Put the wine bottle on the top of the drawer"
    raw_mapping["put the wine bottle on top of the drawer"] = orig_3         # DEFAULT (no "the")
