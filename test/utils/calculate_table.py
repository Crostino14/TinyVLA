    raw_mapping["Place the wine bottle on the top of the cabinet"] = orig_3  # L1: verb swap (put → place)
    raw_mapping["Top of the cabinet needs to have the wine bottle on it"] = orig_3  # L2: passive
    raw_mapping["Put the object behind the bowl on the top of the cabinet"] = orig_3  # L3: indirect reference

    # ── Task 4: Open the top layer of the drawer and put the bowl inside ──
    orig_4 = "Open the top layer of the drawer and put the bowl inside"
