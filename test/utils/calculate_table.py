"""
generate_excel_comparison.py
=============================

Overview
--------
This script parses TinyVLA evaluation log files (.txt) generated by LIBERO
benchmark evaluation runs and aggregates their results into a formatted Excel
(.xlsx) comparison table.

It supports:
  - Evaluation results across **3 random seeds** (seed 0, 1, 2)
  - **Multiple log files per seed** (e.g., when tasks are split across jobs)
  - Both **default** task commands and **linguistic variation** commands
    (L1: paraphrase, L2: passive/descriptive, L3: referential/indirect)
  - **Case-insensitive matching** of task descriptions via a comprehensive mapping
  - **Automatic file discovery** from a directory (auto-detect mode) or
    **manual file specification** via CLI arguments

Output Excel Format
-------------------
The generated Excel table has 10 columns:
  1.  Original Task Command          — canonical task name
  2.  Variation Task Command         — actual command found in log files
  3.  Success Rate (%) - Seed 0      — formatted as "XX.X%"
  4.  Task Completion - Seed 0       — formatted as "N/50"
  5.  Success Rate (%) - Seed 1
  6.  Task Completion - Seed 1
  7.  Success Rate (%) - Seed 2
  8.  Task Completion - Seed 2
  9.  Mean Success Rate (%) ± Std    — mean and sample std across seeds
  10. Mean Task Completion            — average successes over 50 episodes

A final summary row reports the mean ± std success rate across all tasks
for each seed, and the global mean ± std computed from seed-level averages
(following the standard VLA evaluation reporting convention).

Linguistic Variation Levels
----------------------------
  DEFAULT : Original full task command used during training
  L1      : Lexical paraphrase (synonymous verb/preposition swap)
  L2      : Passive or descriptive rephrasing (syntactic change)
  L3      : Referential expression (object identified indirectly)

Usage
-----
Auto-detect mode (finds files by naming pattern):
    python generate_excel_comparison.py \\
        --txt_dir /path/to/logs \\
        --pattern EVAL-libero_goal-tiny_vla \\
        output.xlsx

Manual mode (explicitly specify files per seed):
    python generate_excel_comparison.py \\
        --manual \\
        --seed0 seed0_part1.txt seed0_part2.txt \\
        --seed1 seed1.txt \\
        --seed2 seed2.txt \\
        output.xlsx

Dependencies
------------
  - openpyxl   : Excel file creation and formatting
  - re         : Regular expression parsing of log file lines
  - math       : Standard deviation computation
  - glob       : File pattern matching for auto-detect
  - argparse   : Command-line argument parsing
  - os         : Filesystem path utilities
  - collections: defaultdict for multi-file aggregation
"""

