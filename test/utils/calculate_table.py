    orig_6 = "Push the plate to the front of the stove"
    raw_mapping["push the plate to the front of the stove"] = orig_6       # DEFAULT
    raw_mapping["Move the plate to the front of the stove"] = orig_6       # L1: verb swap (push → move)
    raw_mapping["The space in front of the stove needs to have the plate in it"] = orig_6  # L2: descriptive
    raw_mapping["Push the object in front of the drawer to the front of the stove"] = orig_6  # L3: indirect

    # ── Task 7: Put the cream cheese in the bowl ──────────────────────────
    orig_7 = "Put the cream cheese in the bowl"
    raw_mapping["Put the cream cheese in the bowl"] = orig_7               # DEFAULT
    raw_mapping["put the cream cheese on the bowl"] = orig_7               # Alternate default (on vs in)
    raw_mapping["Place the cream cheese on the bowl"] = orig_7             # L1: verb swap + "on"
    raw_mapping["Place the cream cheese in the bowl"] = orig_7             # L1 alt: verb swap + "in"
    raw_mapping["The cream cheese needs to be put on the bowl"] = orig_7   # L2: passive
    raw_mapping["Put the object in front of the stove on the bowl"] = orig_7  # L3: indirect reference

    # ── Task 8: Turn on the stove ─────────────────────────────────────────
    orig_8 = "Turn on the stove"
    raw_mapping["turn on the stove"] = orig_8                              # DEFAULT
    raw_mapping["Switch on the stove"] = orig_8                            # L1: verb swap (turn → switch)
