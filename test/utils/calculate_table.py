def write_excel_comparison(output_xlsx, txt_files_by_seed):
    """
    Parse all seed log files, aggregate results, and write the formatted Excel table.

    This is the main reporting function. It orchestrates the full pipeline:
      1. Parse and merge log files for each of the 3 seeds.
      2. Map every variation command to its canonical original task description.
      3. Iterate over the fixed canonical task order.
      4. For each task and seed, extract success rate and compute N/50 completion.
      5. Compute per-task mean ± sample std across seeds.
      6. Append a final summary row with per-seed and global mean ± std.
      7. Auto-adjust column widths and save the workbook.

    Parameters
    ----------
    output_xlsx : str
        Output file path for the generated Excel workbook (e.g., "results.xlsx").
    txt_files_by_seed : dict
        Dictionary mapping seed index (int: 0, 1, 2) → list of file path strings.
        Example:
          {
            0: ["logs/seed0_tasks1-5.txt", "logs/seed0_tasks6-10.txt"],
            1: ["logs/seed1_all.txt"],
            2: ["logs/seed2_all.txt"]
          }

    Returns
    -------
    None
        Writes the Excel file to `output_xlsx` and prints status messages.

    Notes
    -----
    Global Mean ± Std Computation (Standard VLA Convention):
    ─────────────────────────────────────────────────────────
    Rather than computing the standard deviation across all individual task×seed
    combinations, the global std is computed across the per-seed means:
      1. Compute mean_seed_k = mean(all task rates for seed k)   for k in {0,1,2}
      2. global_mean = mean(mean_seed_0, mean_seed_1, mean_seed_2)
      3. global_std  = sample_std(mean_seed_0, mean_seed_1, mean_seed_2)

    This convention matches how VLA papers report aggregate performance
    (variability across seeds, not across tasks).

    NaN Handling:
    ─────────────
    If a task is missing from a seed's log files, its rate is set to float('nan').
    NaN values are excluded from mean/std computations. The completion cell
    defaults to "0/50" for missing tasks.
    """
    # ── Step 1: Parse and merge all log files per seed ─────────────────────
    all_rates = []      # List of 3 dicts: all_rates[seed_idx][task] = rate (%)
    all_episodes = []   # List of 3 dicts: all_episodes[seed_idx][task] = episode_count
    task_order = None   # Will be set from the first seed's task order

    print("\n[INFO] Parsing e merge dei file txt...")
    for seed_idx in range(3):
        filepaths = txt_files_by_seed[seed_idx]
        print(f"  - Seed {seed_idx}: {len(filepaths)} file")
        for fp in filepaths:
            print(f"    • {os.path.basename(fp)}")  # Print filename only (not full path)

        # Merge all files for this seed into a single rates dict
        rates, episodes, order = merge_txt_files(filepaths)
        all_rates.append(rates)
        all_episodes.append(episodes)

        # Use the first seed's task order as the reference order
        if task_order is None:
            task_order = order

        print(f"    ✓ Trovati {len(rates)} task totali")

    # ── Step 2: Build variation → original mapping ─────────────────────────
    variation_to_original = get_variation_mapping()  # Lowercase keys → canonical task strings

    # Invert the mapping: find which variation string was actually found in the files
    # for each canonical original task. This is the string that will be displayed
    # in the "Variation Task Command" column of the Excel table.
    original_to_variation = {}
    print("\n[INFO] Identificazione variazioni nei file...")
    for task in task_order:
        task_lower = task.lower()  # Normalize to lowercase for case-insensitive lookup
        if task_lower in variation_to_original:
            orig = variation_to_original[task_lower]       # Get the canonical task name
            original_to_variation[orig] = task             # Map canonical → actual variation string
            print(f"  ✓ '{task}' → '{orig}'")
        else:
            # Task not found in mapping: treat itself as its own canonical form
            print(f"  ⚠ Non mappata: '{task}'")
            original_to_variation[task] = task  # Fallback: use the task as its own "variation"

    # ── Step 3: Use the fixed canonical task order for consistent row layout ──
    fixed_task_order = get_task_order()

    # ── Step 4: Create Excel workbook ─────────────────────────────────────
    wb = Workbook()
    ws = wb.active
    ws.title = "Comparison"  # Name the active worksheet

    # ── Step 5: Write column header row ───────────────────────────────────
    ws.append([
        "Original Task Command",
        "Variation Task Command",
        "Success Rate (%) - Seed 0",
        "Task Completion - Seed 0",
        "Success Rate (%) - Seed 1",
        "Task Completion - Seed 1",
        "Success Rate (%) - Seed 2",
        "Task Completion - Seed 2",
        "Mean Success Rate (%) ± Std",
        "Mean Task Completion"
    ])

    # Apply bold font and center+wrap alignment to all header cells
    for cell in ws[1]:
        cell.font = Font(bold=True)
        cell.alignment = Alignment(horizontal='center', wrap_text=True)

    # ── Step 6: Accumulators for the final summary row ─────────────────────
    # all_seed_rates[k]       : list of per-task success rates for seed k
    # all_seed_completions[k] : list of (success_count, episode_count) tuples for seed k
    all_seed_rates = [[], [], []]
    all_seed_completions = [[], [], []]

    print("\n[INFO] Generazione tabella Excel...")

    # ── Step 7: Process each canonical task in fixed order ─────────────────
    for orig_task in fixed_task_order:
        # Find the actual variation string used in the log files for this canonical task
        variation = original_to_variation.get(orig_task, orig_task)

        seed_rates = []         # Success rates (%) for this task, one per seed
        seed_completions = []   # "N/ep" strings for this task, one per seed
        seed_success_counts = []  # Integer success counts per seed
        seed_episode_counts = []  # Episode counts per seed

        # ── Extract data for each seed ─────────────────────────────────────
        for seed_idx in range(3):
            if variation in all_rates[seed_idx]:
                # Task found in this seed's data
                rate = all_rates[seed_idx][variation]             # Success rate in %
                ep = all_episodes[seed_idx][variation]             # Episode count (50)

                # Compute integer success count from rate and episode count
                success_count = int(round(rate / 100 * ep))

                seed_rates.append(rate)
                seed_completions.append(f"{success_count}/{ep}")
                seed_success_counts.append(success_count)
                seed_episode_counts.append(ep)

                # Accumulate for per-seed summary statistics
                all_seed_rates[seed_idx].append(rate)
                all_seed_completions[seed_idx].append((success_count, ep))
            else:
                # Task missing from this seed's log files: use NaN / zero placeholder
                seed_rates.append(float('nan'))
                seed_completions.append("0/50")
                seed_success_counts.append(0)
                seed_episode_counts.append(50)

        # ── Compute per-task mean ± sample std across seeds ────────────────
        # Exclude NaN values (tasks missing from some seeds)
        valid_rates = [r for r in seed_rates if r == r]  # NaN != NaN is always True, so this filters NaN

        if valid_rates:
            mean_rate = sum(valid_rates) / len(valid_rates)  # Arithmetic mean

            if len(valid_rates) > 1:
                # Sample standard deviation: divide by (N-1) for unbiased estimate
                std_rate = math.sqrt(
                    sum((r - mean_rate) ** 2 for r in valid_rates) / (len(valid_rates) - 1)
                )
            else:
                std_rate = 0.0  # Cannot compute std with a single value

            mean_std_display = f"{mean_rate:.1f}% ± {std_rate:.1f}%"
        else:
            # All seeds missing this task
            mean_rate = float('nan')
            std_rate = float('nan')
            mean_std_display = "nan%"

        # Truncate variation string if longer than 50 characters to fit Excel column
        var_display = variation if len(variation) <= 50 else variation[:47] + "..."

        # Compute mean task completion: average successes out of 50 episodes
        total_success = sum(seed_success_counts)       # Total successes across all 3 seeds
        total_episodes = sum(seed_episode_counts)      # Total episodes across all 3 seeds

        # Mean successes per seed (based on mean_rate, not raw total)
        avg_success = int(round(mean_rate / 100 * 50))  # e.g., 84.0% → 42/50
        avg_episodes = 50  # Each seed always uses exactly 50 episodes per task

        # ── Append data row to the worksheet ──────────────────────────────
        ws.append([
            orig_task,                  # Column 1: canonical original task name
            var_display,                # Column 2: variation command (truncated if needed)
            # Columns 3-4: Seed 0 — success rate and completion count
            f"{seed_rates[0]:.1f}%" if seed_rates[0] == seed_rates[0] else "nan%",
            seed_completions[0],
            # Columns 5-6: Seed 1
            f"{seed_rates[1]:.1f}%" if seed_rates[1] == seed_rates[1] else "nan%",
            seed_completions[1],
            # Columns 7-8: Seed 2
            f"{seed_rates[2]:.1f}%" if seed_rates[2] == seed_rates[2] else "nan%",
            seed_completions[2],
            mean_std_display,           # Column 9: mean ± std across seeds
            f"{avg_success}/{avg_episodes}"  # Column 10: mean completion as N/50
        ])

    # ── Step 8: Build the final summary row ───────────────────────────────
    # This row summarizes the mean ± std success rate across ALL tasks,
    # separately for each seed and globally.
    final_row = ["Mean% ± Std%", ""]  # Columns 1-2: label (no variation applicable)

    # Per-seed summary statistics
    for seed_idx in range(3):
        rates = all_seed_rates[seed_idx]  # All per-task success rates for this seed

        if rates:
            mean = sum(rates) / len(rates)  # Mean success rate across all tasks for this seed

            if len(rates) > 1:
                # Sample std across all task success rates for this seed
                std = math.sqrt(
                    sum((r - mean) ** 2 for r in rates) / (len(rates) - 1)
                )
            else:
                std = 0.0

            # Total completions: sum of successes and episodes across all tasks for this seed
            total_success = sum(c[0] for c in all_seed_completions[seed_idx])
            total_episodes = sum(c[1] for c in all_seed_completions[seed_idx])

            final_row.append(f"{mean:.2f}% ± {std:.2f}%")          # "XX.XX% ± YY.YY%"
            final_row.append(f"{total_success}/{total_episodes}")    # "N/M"
        else:
            final_row.append("N/A")   # No data for this seed
            final_row.append("0/0")

    # ── Global mean ± std (standard VLA reporting convention) ─────────────
    # Compute the mean of each seed's mean (not the pooled std across all tasks)
    seed_means = []
    for seed_idx in range(3):
        if all_seed_rates[seed_idx]:
            # Per-seed mean: average success rate across all tasks for seed k
            seed_mean = sum(all_seed_rates[seed_idx]) / len(all_seed_rates[seed_idx])
            seed_means.append(seed_mean)

    if seed_means:
        # Global mean: average of the three seed means
        global_mean = sum(seed_means) / len(seed_means)

        if len(seed_means) > 1:
            # Sample std across seed-level means (3 values: one per seed)
            # This is the standard convention in VLA papers for reporting robustness
            global_std = math.sqrt(
                sum((m - global_mean) ** 2 for m in seed_means) / (len(seed_means) - 1)
            )
        else:
            global_std = 0.0

        # Total completions across all seeds and all tasks
        global_success = sum(
            sum(c[0] for c in all_seed_completions[i]) for i in range(3)
        )
        global_episodes = sum(
            sum(c[1] for c in all_seed_completions[i]) for i in range(3)
        )

        final_row.append(f"{global_mean:.2f}% ± {global_std:.2f}%")  # Global mean ± std
        final_row.append(f"{global_success}/{global_episodes}")        # Global N/M
    else:
        final_row.append("N/A")
        final_row.append("0/0")

    ws.append(final_row)  # Append the completed final summary row

    # Apply bold formatting to the final summary row for visual prominence
    last_row = ws.max_row
    for cell in ws[last_row]:
        cell.font = Font(bold=True)

    # ── Step 9: Auto-adjust column widths ─────────────────────────────────
    for column in ws.columns:
        max_length = 0
        column_letter = column[0].column_letter  # Get the Excel column letter (A, B, C, ...)
        for cell in column:
            try:
                if cell.value and len(str(cell.value)) > max_length:
                    max_length = len(str(cell.value))  # Track the longest string in this column
            except:
                pass  # Skip cells that raise exceptions (e.g., merged or formula cells)

        # Set column width: content length + 2 padding, capped at 50 to prevent excessive width
        adjusted_width = min(max_length + 2, 50)
        ws.column_dimensions[column_letter].width = adjusted_width

    # ── Step 10: Save workbook to disk ────────────────────────────────────
    wb.save(output_xlsx)
    print(f"\n[OK] Excel salvato in: {output_xlsx}\n")


