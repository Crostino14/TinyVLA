    raw_mapping["Place the wine bottle on the rack"] = orig_10             # L1: verb swap
    raw_mapping["The rack needs to be filled with the wine bottle in it"] = orig_10  # L2: descriptive
    raw_mapping["Put the object behind the bowl on the rack"] = orig_10    # L3: indirect reference

    # Convert all keys to lowercase to enable case-insensitive matching
    # when comparing against task strings extracted from log files
    mapping = {k.lower(): v for k, v in raw_mapping.items()}

    return mapping


