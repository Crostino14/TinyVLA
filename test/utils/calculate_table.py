def main():
    """
    Command-line entry point for the TinyVLA LIBERO task comparison Excel generator.

    Parses CLI arguments, determines which log files to use (auto-detect or manual),
    and calls `write_excel_comparison` to produce the output `.xlsx` file.

    Two mutually exclusive operating modes are supported:

    **Auto-detect mode** (``--txt_dir``):
      The script searches the given directory for `.txt` files whose names match
      the pattern ``{--pattern}*seed{0,1,2}*.txt``. Files for each seed are
      collected automatically. This is the recommended mode for standard TinyVLA
      evaluation workflows.

    **Manual mode** (``--manual``):
      The user explicitly provides file lists for each seed using ``--seed0``,
      ``--seed1``, and ``--seed2``. This is useful when files do not follow the
      standard naming convention or are located in different directories.

    CLI Arguments
    -------------
    Mutually exclusive group (one is required):
      --txt_dir PATH
          Directory containing all `.txt` log files (auto-detect mode).
      --manual
          Enable manual file specification mode.

    Optional:
      --pattern PREFIX
          Filename prefix for auto-detect matching.
          Default: "EVAL-libero_goal-tiny_vla".
      --seed0 FILE [FILE ...]
          One or more log file paths for seed 0 (manual mode only).
      --seed1 FILE [FILE ...]
          One or more log file paths for seed 1 (manual mode only).
      --seed2 FILE [FILE ...]
          One or more log file paths for seed 2 (manual mode only).

    Positional:
      output_xlsx
          Path for the output Excel file (e.g., "results/comparison.xlsx").

    Exit Codes
    ----------
    0 : Success — Excel file written successfully.
    1 : Error — manual mode used without specifying all three seed file lists,
        or auto-detect failed to find files for all seeds.

    Examples
    --------
    Auto-detect:
        python generate_excel_comparison.py \\
            --txt_dir /mnt/logs \\
            --pattern EVAL-libero_goal-tiny_vla \\
            output.xlsx

    Manual (multiple files per seed):
        python generate_excel_comparison.py \\
            --manual \\
            --seed0 logs/seed0_part1.txt logs/seed0_part2.txt \\
            --seed1 logs/seed1.txt \\
            --seed2 logs/seed2.txt \\
            output.xlsx
    """
    parser = argparse.ArgumentParser(
        description="Genera tabella Excel da file txt TinyVLA (supporta file multipli per seed)"
    )

    # Mutually exclusive group: user must choose EITHER --txt_dir OR --manual (not both)
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument(
        "--txt_dir",
        help="Directory contenente i file .txt (auto-detect per seed)"
    )
    group.add_argument(
        "--manual",
        action="store_true",  # Flag: True if --manual is specified, False otherwise
        help="Modalità manuale: specifica file per ogni seed"
    )

    # Optional arguments for file discovery pattern and manual file lists
    parser.add_argument(
        "--pattern",
        default="EVAL-libero_goal-tiny_vla",
        help="Prefix pattern per auto-detect (default: EVAL-libero_goal-tiny_vla)"
    )
    parser.add_argument(
        "--seed0",
        nargs="+",  # Accepts one or more values: "--seed0 file1.txt file2.txt"
        help="File per seed 0 (modalità manuale)"
    )
    parser.add_argument(
        "--seed1",
        nargs="+",
        help="File per seed 1 (modalità manuale)"
    )
    parser.add_argument(
        "--seed2",
        nargs="+",
        help="File per seed 2 (modalità manuale)"
    )
    parser.add_argument(
        "output_xlsx",
        help="File Excel di output"
    )

    args = parser.parse_args()

    # Print ASCII banner to identify the script and its capabilities
    print("\n" + "=" * 60)
    print("  TINYVLA LIBERO TASK COMPARISON FROM TXT FILES")
    print("  (Supporting Multiple Files Per Seed)")
    print("=" * 60)

    # ── Determine the file-to-seed mapping based on the chosen mode ────────
    if args.manual:
        # Manual mode: all three --seedN arguments must be provided
        if not (args.seed0 and args.seed1 and args.seed2):
            print("\n[ERROR] Modalità manuale richiede --seed0, --seed1, --seed2!")
            exit(1)  # Exit with error code 1
        # Build the seed file mapping from CLI arguments
        txt_files_by_seed = {
            0: args.seed0,   # List of file paths for seed 0
            1: args.seed1,   # List of file paths for seed 1
            2: args.seed2    # List of file paths for seed 2
        }
    else:
        # Auto-detect mode: scan the directory for matching files
        txt_files_by_seed = find_txt_files_by_seed(args.txt_dir, args.pattern)
        if txt_files_by_seed is None:
            # find_txt_files_by_seed returns None if any seed has no matching files
            print("\n[ERROR] Non ho trovato file per tutti e 3 i seed!")
            exit(1)

    # ── Generate the Excel comparison table ────────────────────────────────
    write_excel_comparison(args.output_xlsx, txt_files_by_seed)

    # Print completion banner
    print("=" * 60)
    print("  COMPLETATO!")
    print("=" * 60 + "\n")


if __name__ == "__main__":
    main()  # Invoke main() only when this script is executed directly (not imported)