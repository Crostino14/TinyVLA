def main():
    """
    Command-line entry point for the TinyVLA Task Composition Excel table generator.

    Parses CLI arguments to determine the composition level, file discovery mode,
    and output path. Then calls `write_excel()` to produce the final `.xlsx` report.

    Two mutually exclusive operating modes are supported:

    **Auto-detect mode** (``--txt_dir``):
      Scans the given directory for `.txt` files matching the pattern
      ``{--pattern}*seed_{0,1,2}*.txt`` (with underscore fallback).
      The default pattern is automatically constructed from ``--level``.
      This is the recommended mode for standard evaluation workflows.

    **Manual mode** (``--manual``):
      The user provides explicit file lists for each seed via ``--seed0``,
      ``--seed1``, and ``--seed2``. Useful when files do not follow the
      standard naming convention or are in different directories.

    CLI Arguments
    -------------
    Mutually exclusive group (exactly one required):
      --txt_dir PATH
          Directory containing all `.txt` log files (auto-detect mode).
      --manual
          Enable manual file specification mode.

    Optional:
      --level {l1, l2}
          Task composition level to process. Default: "l1".
      --pattern PREFIX
          Custom filename prefix for auto-detect (overrides the default
          "EVAL-task_comp_{level}" pattern).
      --seed0 FILE [FILE ...]
          One or more log file paths for seed 0 (manual mode only).
      --seed1 FILE [FILE ...]
          One or more log file paths for seed 1 (manual mode only).
      --seed2 FILE [FILE ...]
          One or more log file paths for seed 2 (manual mode only).

    Positional (required):
      output_xlsx
          Output path for the generated Excel file (e.g., "results/l1.xlsx").

    Exit Codes
    ----------
    0 : Success — Excel file written successfully.
    1 : Error — manual mode used without all three --seedN arguments, or
        auto-detect failed to find files for all seeds.

    Examples
    --------
    Auto-detect L1:
        python calculate_table_task_comp.py \\
            --level l1 --txt_dir /mnt/logs results_l1.xlsx

    Auto-detect L2 with custom pattern:
        python calculate_table_task_comp.py \\
            --level l2 --txt_dir /mnt/logs \\
            --pattern MY-EVAL-task_comp_l2 results_l2.xlsx

    Manual mode:
        python calculate_table_task_comp.py \\
            --manual \\
            --seed0 logs/seed0.txt \\
            --seed1 logs/seed1.txt \\
            --seed2 logs/seed2.txt \\
            results.xlsx
    """
    parser = argparse.ArgumentParser(
        description="Genera tabella Excel Task Composition L1/L2 (TinyVLA) da file log .txt"
    )

    # Mutually exclusive group: only one input mode may be selected
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument(
        "--txt_dir",
        help="Directory con i file .txt (auto-detect)"
    )
    group.add_argument(
        "--manual",
        action="store_true",  # Boolean flag: True if --manual is present
        help="Specifica file manualmente"
    )

    parser.add_argument(
        "--level",
        default="l1",
        choices=["l1", "l2"],  # Only these two values are valid
        help="Livello di composizione: l1 (default) o l2"
    )
    parser.add_argument(
        "--pattern",
        default=None,  # None triggers automatic default construction in find_txt_files_by_seed
        help="Prefix per auto-detect (default: EVAL-task_comp_<level>)"
    )
    parser.add_argument(
        "--seed0",
        nargs="+",  # Accepts one or more values
        help="File per seed 0 (modo manuale)"
    )
    parser.add_argument(
        "--seed1",
        nargs="+",
        help="File per seed 1 (modo manuale)"
    )
    parser.add_argument(
        "--seed2",
        nargs="+",
        help="File per seed 2 (modo manuale)"
    )
    parser.add_argument(
        "output_xlsx",
        help="File Excel di output"
    )

    args = parser.parse_args()

    # Print ASCII banner to identify the script and current level
    print("\n" + "=" * 60)
    print(f"  TASK COMPOSITION {args.level.upper()} (TinyVLA) - EXCEL TABLE GENERATOR")
    print("=" * 60)

    # ── Determine the seed-to-file mapping based on the chosen mode ────────
    if args.manual:
        # Manual mode: validate that all three seed file lists were provided
        if not (args.seed0 and args.seed1 and args.seed2):
            print("[ERROR] --manual richiede --seed0, --seed1, --seed2")
            exit(1)  # Exit with error code 1

        # Build the mapping from explicitly provided file lists
        txt_files_by_seed = {
            0: args.seed0,   # List of file paths for seed 0
            1: args.seed1,   # List of file paths for seed 1
            2: args.seed2    # List of file paths for seed 2
        }
    else:
        # Auto-detect mode: scan the directory for matching files
        txt_files_by_seed = find_txt_files_by_seed(
            args.txt_dir,
            args.pattern,  # None triggers default pattern construction
            args.level
        )
        if txt_files_by_seed is None:
            # find_txt_files_by_seed returns None if any seed has no files
            print("[ERROR] File non trovati per tutti i seed!")
            exit(1)

    # ── Generate the Excel table ───────────────────────────────────────────
    write_excel(args.output_xlsx, txt_files_by_seed, level=args.level)

    # Print completion banner
    print("=" * 60)
    print("  COMPLETATO!")
    print("=" * 60 + "\n")


if __name__ == "__main__":
    main()  # Execute main() only when run as a script (not when imported as a module)