def find_txt_files_by_seed(base_dir, pattern_prefix=None, level="l1"):
    """
    Automatically discover TinyVLA task composition log files organized by seed.

    Searches `base_dir` for `.txt` files matching the naming convention used by
    the TinyVLA task composition evaluation pipeline:
        {pattern_prefix}*seed_{seed_idx}*.txt

    A fallback pattern without the underscore before the seed number is also
    tried if the primary pattern yields no results:
        {pattern_prefix}*seed{seed_idx}*.txt

    This handles both `seed_0` and `seed0` naming variants that may appear
    depending on the evaluation script version used.

    Parameters
    ----------
    base_dir : str
        Directory path in which to search for log files.
    pattern_prefix : str or None, optional
        Filename prefix shared by all log files. If None, defaults to
        "EVAL-task_comp_{level}" (e.g., "EVAL-task_comp_l1"). This matches
        the naming convention of `run_libero_eval_task_comp.py` outputs.
    level : str, optional
        Composition level ("l1" or "l2"). Used to construct the default
        `pattern_prefix` when none is explicitly provided. Default: "l1".

    Returns
    -------
    dict or None
        Dictionary mapping seed index (int: 0, 1, 2) → sorted list of matching
        file path strings. Returns None if any seed has zero matching files,
        allowing the caller to handle the error before proceeding.

    Notes
    -----
    - Files within each seed are sorted alphabetically, which typically
      corresponds to the task group order for split evaluation jobs.
    - The two-step pattern matching (primary with underscore, fallback without)
      ensures compatibility across different evaluation script versions.
    - Returning None (rather than raising an exception) allows `main()` to
      print a user-friendly error message and exit cleanly.

    Example
    -------
    >>> files = find_txt_files_by_seed("/logs", level="l1")
    >>> files[0]
    ['/logs/EVAL-task_comp_l1-2025-06-01--seed_0--part1.txt']
    """
    # Set the default pattern prefix based on the composition level
    if pattern_prefix is None:
        pattern_prefix = f"EVAL-task_comp_{level}"  # e.g., "EVAL-task_comp_l1"

    txt_files_by_seed = {0: [], 1: [], 2: []}  # Initialize empty file lists per seed

    print(f"\n[INFO] Cercando in: {base_dir}")

    for seed_idx in range(3):
        # Primary pattern: "seed_N" with underscore separator (newer naming convention)
        pattern = os.path.join(base_dir, f"{pattern_prefix}*seed_{seed_idx}*.txt")
        matches = sorted(glob.glob(pattern))  # Sorted for deterministic ordering

        # Fallback pattern: "seedN" without underscore (older naming convention)
        if not matches:
            pattern = os.path.join(base_dir, f"{pattern_prefix}*seed{seed_idx}*.txt")
            matches = sorted(glob.glob(pattern))

        txt_files_by_seed[seed_idx] = matches

        if matches:
            print(f"  Seed {seed_idx}: {len(matches)} file")
            for m in matches:
                print(f"    - {os.path.basename(m)}")  # Display only filename for readability
        else:
            # Critical failure: missing files for this seed cannot be recovered
            print(f"  Seed {seed_idx}: NESSUN FILE!")
            return None  # Signal failure to the caller

    return txt_files_by_seed


