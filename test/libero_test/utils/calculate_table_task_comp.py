"""
calculate_table_task_comp.py
=============================

Overview
--------
This script parses TinyVLA evaluation log files (.txt) generated by the
task composition evaluation pipeline (`run_libero_eval_task_comp.py`) and
aggregates their results into a structured, formatted Excel (.xlsx) table.

It supports two task composition difficulty levels:

  - **L1 (Compositional Generalization Level 1)**:
    The model must apply known manipulation primitives (pick-place, open drawer)
    to NEW object-target combinations never seen together during training.
    Example: trained on "plate → stove" and "bowl → cabinet", tested on
    "plate → cabinet" (novel object-target pair).

  - **L2 (Compositional Generalization Level 2)**:
    Multi-step chained tasks combining two sequential primitives. The model
    must execute longer-horizon behaviors not seen as a unit during training.
    Example: "Put the bowl on the stove AND turn on the stove".

Output Excel Format
-------------------
Each row corresponds to one task composition task. The 11 columns are:

  1.  # Task               — 1-based task index
  2.  Task Command         — the full task description tested
  3.  Reference Task       — training-time task(s) whose primitives are composed
  4.  SR% - Seed 0         — success rate for seed 0 (e.g., "84.0%")
  5.  Task Completion - S0 — N/50 completion for seed 0
  6.  SR% - Seed 1         — success rate for seed 1
  7.  Task Completion - S1 — N/50 completion for seed 1
  8.  SR% - Seed 2         — success rate for seed 2
  9.  Task Completion - S2 — N/50 completion for seed 2
  10. Mean SR% ± Std       — arithmetic mean ± sample std across seeds
  11. Mean Task Completion — average N/50 based on mean success rate

A final summary row reports per-seed means and the global mean ± std
computed from seed-level averages (standard VLA reporting convention).

Task Composition Levels — Fixed Task Lists
-------------------------------------------
L1 Tasks (5 tasks, all in libero_goal scene):
  1. Put the plate on the top of the cabinet
  2. Put the plate on the stove
  3. Put the cream cheese on the top of the cabinet
  4. Put the cream cheese on the plate
  5. Open the top layer of the drawer and put the cream cheese inside

L2 Tasks (5 tasks, chained operations):
  1. Open the middle layer of the drawer and put the bowl inside
  2. Put the bowl on the stove and turn on the stove
  3. Put the cream cheese on the bowl and put the bowl on the plate
  4. Push the plate to the front of the stove and put the bowl on the plate
  5. Put the cream cheese on the bowl and put the bowl on the top of the cabinet

Dependencies
------------
  - openpyxl     : Excel workbook creation and cell formatting
  - re           : Regular expression matching for log file parsing
  - math         : Standard deviation computation
  - glob         : File pattern expansion for auto-detect mode
  - argparse     : Command-line argument parsing
  - os           : Filesystem path utilities
  - collections  : defaultdict for multi-file aggregation

Usage
-----
Auto-detect mode (recommended):
    python calculate_table_task_comp.py --txt_dir /path/to/logs output.xlsx
    python calculate_table_task_comp.py --level l2 --txt_dir /path/to/logs output.xlsx

Manual mode (specify files per seed explicitly):
    python calculate_table_task_comp.py \\
        --manual \\
        --seed0 seed0.txt \\
        --seed1 seed1.txt \\
        --seed2 seed2.txt \\
        output.xlsx
"""

import re           # Regular expressions for extracting numeric values from log lines
import math         # Used for computing sample standard deviation (math.sqrt)
import argparse     # Command-line interface argument parsing
import os           # OS-level path operations (basename, join, makedirs)
import glob         # Unix-style wildcard file path expansion
from collections import defaultdict  # Dictionary with automatic default value factory
from openpyxl import Workbook        # Creates and manages Excel workbook objects
from openpyxl.styles import Font, Alignment  # Cell formatting: bold text, alignment


# =========================
# TASK ORDER & MAPPING
# =========================


def normalize_task_key(task_name):
    """
    Normalize a task description string into a canonical lowercase key.

    This function enables robust, case-insensitive matching between task
    descriptions as they appear in log files (which may vary in capitalization
    or have extra whitespace) and the hardcoded task names in the reference
    dictionaries.

    The normalization pipeline:
      1. Strip leading and trailing whitespace from the input string.
      2. Convert the entire string to lowercase.
      3. Collapse all internal whitespace sequences to a single space.

    Parameters
    ----------
    task_name : str
        The raw task description string to normalize. May contain mixed case,
        leading/trailing spaces, or irregular internal spacing.

    Returns
    -------
    str
        A normalized lowercase string with a single space between words.

    Examples
    --------
    >>> normalize_task_key("  Put The Bowl On The Stove  ")
    'put the bowl on the stove'
    >>> normalize_task_key("put  the  bowl  on  the  stove")
    'put the bowl on the stove'
    """
    return " ".join(task_name.strip().lower().split())


def get_task_comp_l1_order():
    """
    Return the fixed ordered list of 5 Level-1 task composition task descriptions.

    These five tasks represent compositional generalization at Level 1:
    the model is evaluated on object-target combinations that combine individually
    known object types and target locations but were never seen together in training.

    The order is used to control the row sequence in the generated Excel table,
    ensuring consistent layout regardless of how tasks appear in log files.

    Composition rationale for each task:
      1. Put the plate on the top of the cabinet
         → Training: plate manipulation + bowl→cabinet; novel: plate→cabinet
      2. Put the plate on the stove
         → Training: plate push + bowl→stove; novel: pick-place plate→stove
      3. Put the cream cheese on the top of the cabinet
         → Training: cream cheese→bowl + bowl→cabinet; novel: cream cheese→cabinet
      4. Put the cream cheese on the plate
         → Training: cream cheese→bowl + bowl→plate; novel: cream cheese→plate
      5. Open the top layer of the drawer and put the cream cheese inside
         → Training: open drawer + bowl inside; novel: cream cheese inside (same primitive)

    Returns
    -------
    list of str
        Five canonical L1 task description strings in their fixed display order.
    """
    return [
        "Put the plate on the top of the cabinet",
        "Put the plate on the stove",
        "Put the cream cheese on the top of the cabinet",
        "Put the cream cheese on the plate",
        "Open the top layer of the drawer and put the cream cheese inside",
    ]


def get_task_comp_l2_order():
    """
    Return the fixed ordered list of 5 Level-2 task composition task descriptions.

    These five tasks represent compositional generalization at Level 2:
    multi-step chained behaviors where two sequential primitives (each known
    independently) must be executed together in a novel combined sequence.

    The order is used to control the row sequence in the generated Excel table.

    Composition rationale for each task:
      1. Open the middle layer of the drawer and put the bowl inside
         → Training: open middle drawer + open top drawer and put bowl inside
      2. Put the bowl on the stove and turn on the stove
         → Training: put bowl on stove + turn on stove (novel chain)
      3. Put the cream cheese on the bowl and put the bowl on the plate
         → Training: cream cheese→bowl + bowl→plate (two pick-place in sequence)
      4. Push the plate to the front of the stove and put the bowl on the plate
         → Training: push plate + put bowl on plate (push + pick-place chain)
      5. Put the cream cheese on the bowl and put the bowl on the top of the cabinet
         → Training: cream cheese→bowl + bowl→cabinet (two sequential pick-place)

    Returns
    -------
    list of str
        Five canonical L2 task description strings in their fixed display order.
    """
    return [
        "Open the middle layer of the drawer and put the bowl inside",
        "Put the bowl on the stove and turn on the stove",
        "Put the cream cheese on the bowl and put the bowl on the plate",
        "Push the plate to the front of the stove and put the bowl on the plate",
        "Put the cream cheese on the bowl and put the bowl on the top of the cabinet",
    ]


def get_reference_mapping(level="l1"):
    """
    Build and return a mapping from each task composition task to its training
    reference task(s), whose primitives are recombined in the evaluated task.

    Each entry explains the compositional lineage: which training tasks
    the model must generalize from to successfully complete the new task.
    This mapping is displayed in the "Reference Task" column of the Excel table,
    providing interpretability for each evaluation result.

    All dictionary keys are normalized to lowercase via `normalize_task_key`
    to enable case-insensitive lookup when matching against log file strings.

    Parameters
    ----------
    level : str, optional
        Composition level to retrieve the mapping for.
        Must be "l1" (default) or "l2".

    Returns
    -------
    dict
        Mapping from normalized task description string (str) → reference task
        string (str). For L2 tasks, the reference is a " / "-separated pair
        of training task names. For L1 tasks, it is a single training task.

    Notes
    -----
    L1 Reference Interpretation:
      Each L1 composition task reuses one primitive (action type) from training
      but applies it to a novel object or target. The reference task is the
      closest training task that shares the key manipulation primitive.

    L2 Reference Interpretation:
      Each L2 composition task chains two distinct primitives. Both source
      training tasks are listed, separated by " / ", to show which behaviors
      need to be combined.

    Examples
    --------
    >>> mapping = get_reference_mapping("l1")
    >>> mapping[normalize_task_key("Put the plate on the stove")]
    'Put the bowl on the stove'
    >>> mapping = get_reference_mapping("l2")
    >>> mapping[normalize_task_key("Put the bowl on the stove and turn on the stove")]
    'Put the bowl on the stove  /  Turn on the stove'
    """
    if level == "l2":
        # L2: each task combines two chained training primitives
        return {
            normalize_task_key("Open the middle layer of the drawer and put the bowl inside"):
                # Chain: open middle drawer (novel drawer layer) + put bowl inside (known)
                "Open the middle drawer of the cabinet  /  Open the top drawer and put the bowl inside",

            normalize_task_key("Put the bowl on the stove and turn on the stove"):
                # Chain: pick-place bowl → stove (known) + turn on stove (known)
                "Put the bowl on the stove  /  Turn on the stove",

            normalize_task_key("Put the cream cheese on the bowl and put the bowl on the plate"):
                # Chain: cream cheese → bowl (known) + bowl → plate (known)
                "Put the cream cheese in the bowl  /  Put the bowl on the plate",

            normalize_task_key("Push the plate to the front of the stove and put the bowl on the plate"):
                # Chain: push plate → stove front (known) + put bowl on plate (known)
                "Push the plate to the front of the stove  /  Put the bowl on the plate",

            normalize_task_key("Put the cream cheese on the bowl and put the bowl on the top of the cabinet"):
                # Chain: cream cheese → bowl (known) + bowl → cabinet top (known)
                "Put the cream cheese in the bowl  /  Put the bowl on the top of the cabinet",
        }

    # L1: each task applies a known primitive to a novel object-target combination
    return {
        normalize_task_key("Put the plate on the top of the cabinet"):
            # Training: bowl → cabinet; novel: plate replaces bowl
            "Put the bowl on the top of the cabinet",

        normalize_task_key("Put the plate on the stove"):
            # Training: bowl → stove; novel: plate replaces bowl
            "Put the bowl on the stove",

        normalize_task_key("Put the cream cheese on the top of the cabinet"):
            # Training: wine bottle → cabinet; novel: cream cheese replaces wine bottle
            "Put the wine bottle on the top of the cabinet",

        normalize_task_key("Put the cream cheese on the plate"):
            # Training: cream cheese → bowl; novel: plate replaces bowl as target
            "Put the cream cheese on the bowl",

        normalize_task_key("Open the top layer of the drawer and put the cream cheese inside"):
            # Training: open drawer + bowl inside; novel: cream cheese replaces bowl
            "Open the top layer of the drawer and put the bowl inside",
    }


