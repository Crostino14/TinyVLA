def parse_txt_file(filepath):
    """
    Parse a single TinyVLA task composition evaluation log file.

    Extracts per-task success rates and episode counts by scanning the log
    for structured marker lines. Supports two log format variants:
      - **TinyVLA format**: uses "Command:" and "# episodes:" markers
      - **OpenVLA format**: uses "# episodes completed:" instead

    Expected log structure for each task block:
    ::

        COMMAND: Put the plate on the stove
        ...
        # episodes: 50               ← TinyVLA format
        # episodes completed: 50     ← OpenVLA format (alternative)
        ...
        Task success rate: 0.8400    ← decimal value; terminates the block

    Parameters
    ----------
    filepath : str
        Absolute or relative path to the `.txt` log file to parse.

    Returns
    -------
    rates : dict
        Mapping from normalized lowercase task key (str) → success rate in
        percent (float). Example: {"put the plate on the stove": 84.0}
    episodes : dict
        Mapping from normalized lowercase task key (str) → number of episodes
        run (int). Defaults to 50 if the episode count line was not found.
    task_order : list of str
        Task description strings in their original capitalization, in the order
        they first appear in the log file. Used for display in the Excel table.

    Notes
    -----
    - Duplicate task entries (same task appearing multiple times in one file)
      are silently skipped; only the first occurrence is stored.
    - The "# episodes completed:" variant check uses `"completed" not in line_s`
      to distinguish it from the simpler "# episodes:" pattern, preventing
      double-counting when only one format is present.
    - Success rates are read as decimals (0.8400) and converted to percentages
      (84.0) for uniform representation across the rest of the pipeline.
    - `normalize_task_key` is applied to all task keys for robust matching.
    """
    rates = {}          # normalized_key → success rate (%)
    episodes = {}       # normalized_key → episode count
    task_order = []     # original-case task descriptions in encounter order

    with open(filepath, "r", encoding="utf-8") as f:
        lines = f.readlines()

    current_task = None         # Original-case task description of the current block
    current_task_lower = None   # Normalized lowercase key of the current block
    current_episodes = None     # Episode count for the current task block

    for line in lines:
        line_s = line.strip()  # Remove leading/trailing whitespace and newline

        # ── Detect task command marker ─────────────────────────────────────
        # "Command: <task_description>" marks the start of a new task block.
        # `split("Command:", 1)` limits to one split to handle colons in the description.
        if line_s.startswith("Command:"):
            current_task = line_s.split("Command:", 1)[-1].strip()
            current_task_lower = normalize_task_key(current_task)  # Normalize for dict key

        # ── Extract OpenVLA-style episode count ────────────────────────────
        # "# episodes completed: N" — used by OpenVLA evaluation logs
        if current_task and "# episodes completed:" in line_s:
            m = re.search(r"# episodes completed:\s*(\d+)", line_s)
            if m:
                current_episodes = int(m.group(1))  # Convert matched string to integer

        # ── Extract TinyVLA-style episode count ───────────────────────────
        # "# episodes: N" — used by TinyVLA evaluation logs
        # The `"completed" not in line_s` guard prevents matching
        # "# episodes completed:" again in case both lines appear.
        if current_task and "# episodes:" in line_s and "completed" not in line_s:
            m = re.search(r"# episodes:\s*(\d+)", line_s)
            if m:
                current_episodes = int(m.group(1))

        # ── Extract success rate and finalize task block ───────────────────
        # "Task success rate: X.XXXX" marks the end of the current task block.
        if current_task and "Task success rate:" in line_s:
            m = re.search(r"Task success rate:\s*([\d.]+)", line_s)
            if m:
                rate_pct = float(m.group(1)) * 100  # Convert decimal → percentage

                if current_task_lower not in rates:  # Skip if task was already recorded
                    rates[current_task_lower] = rate_pct
                    episodes[current_task_lower] = current_episodes if current_episodes else 50
                    task_order.append(current_task)  # Preserve original capitalization for display

                # Reset parser state: ready for the next task block
                current_task = None
                current_task_lower = None
                current_episodes = None

    return rates, episodes, task_order


def merge_txt_files(txt_files_list):
    """
    Merge multiple TinyVLA log files belonging to the same random seed.

    When a single evaluation run is split across multiple log files (e.g.,
    tasks 1-3 in one job and tasks 4-5 in another), this function aggregates
    all results. If the same task appears in multiple files, its success rates
    are averaged (arithmetic mean over the number of occurrences).

    Parameters
    ----------
    txt_files_list : list of str
        File paths to `.txt` log files for a single seed. One or more files.

    Returns
    -------
    merged_rates : dict
        Mapping from normalized task key (str) → averaged success rate in %
        (float). Example: {"put the plate on the stove": 84.0}
    task_episodes : dict
        Mapping from normalized task key (str) → episode count (int).
        Taken from the last file that reported this task (always 50 in practice).
    all_tasks : list of str
        Original-case task descriptions in the order first encountered across
        all files. Used for ordering in the Excel table.

    Notes
    -----
    - `defaultdict(list)` allows appending multiple success rate values for
      the same task key without initialization checks.
    - The arithmetic mean across files corrects for any task that was run
      partially across multiple jobs, ensuring a single representative rate.
    - Episode count uses the last value seen (via `eps.get(tk, 50)`) since
      all evaluations use exactly 50 episodes per task.
    """
    task_rates_list = defaultdict(list)  # normalized_key → list of rates from each file
    task_episodes = {}    # normalized_key → episode count
    all_tasks = []        # Original-case task descriptions in encounter order

    for fp in txt_files_list:
        rates, eps, order = parse_txt_file(fp)  # Parse one file

        for task in order:
            tk = normalize_task_key(task)  # Use normalized key as the dict key

            if tk not in task_rates_list:
                all_tasks.append(task)  # Record original-case name on first encounter

            task_rates_list[tk].append(rates[tk])  # Accumulate this file's rate
            task_episodes[tk] = eps.get(tk, 50)    # Update episode count (always 50)

    # Compute the arithmetic mean of all accumulated rates per task key
    merged_rates = {tk: sum(v) / len(v) for tk, v in task_rates_list.items()}

    return merged_rates, task_episodes, all_tasks


