def write_excel(output_xlsx, txt_files_by_seed, level="l1"):
    """
    Parse all seed log files, aggregate results, and write the formatted Excel table.

    Orchestrates the full reporting pipeline for task composition evaluation:
      1. Parse and merge log files for each of the 3 seeds.
      2. Look up the reference training task for each composition task.
      3. Iterate over the fixed task order (L1 or L2).
      4. For each task and seed: extract rate, compute N/50 completion.
      5. Compute per-task mean ± sample standard deviation across seeds.
      6. Append a final summary row with per-seed and global mean ± std.
      7. Apply bold formatting to header and summary rows.
      8. Auto-fit column widths and save the workbook.

    Parameters
    ----------
    output_xlsx : str
        Output file path for the generated Excel workbook (e.g., "results.xlsx").
    txt_files_by_seed : dict
        Dictionary mapping seed index (int: 0, 1, 2) → list of file path strings.
        Example:
          {
            0: ["logs/seed0_part1.txt"],
            1: ["logs/seed1.txt"],
            2: ["logs/seed2_part1.txt", "logs/seed2_part2.txt"]
          }
    level : str, optional
        Composition level: "l1" (default) for object-target generalization,
        or "l2" for multi-step chained task generalization.

    Returns
    -------
    None
        Writes the Excel file to `output_xlsx` and prints status messages.

    Notes
    -----
    NaN Handling:
    ─────────────
    If a task is missing from a seed's data, its rate is float('nan') and
    its completion defaults to "0/50". NaN values are excluded from mean/std
    computation via the `r == r` identity (NaN is not equal to itself in
    IEEE 754 arithmetic). Only non-NaN rates are accumulated in `all_seed_rates`.

    Per-Task Mean ± Std:
    ─────────────────────
    Uses the **sample standard deviation** (divides by N-1), appropriate
    for the small sample size of 3 seeds. If only one valid rate is available,
    the std is reported as 0.0.

    Global Mean ± Std (Standard VLA Convention):
    ─────────────────────────────────────────────
    The global std is computed across the per-seed means (not across all
    task×seed combinations):
      seed_mean_k = mean(all task rates for seed k)
      global_mean = mean(seed_mean_0, seed_mean_1, seed_mean_2)
      global_std  = sample_std(seed_mean_0, seed_mean_1, seed_mean_2)

    This matches the reporting convention used in VLA benchmark papers.
    """
    # ── Step 1: Parse and merge log files for each seed ────────────────────
    all_rates = []      # List of 3 dicts: all_rates[seed_idx][tk] = rate (%)
    all_episodes = []   # List of 3 dicts: all_episodes[seed_idx][tk] = episode count

    for seed_idx in range(3):
        fps = txt_files_by_seed[seed_idx]
        print(f"  Seed {seed_idx}: {len(fps)} file")
        for fp in fps:
            print(f"    - {os.path.basename(fp)}")  # Show filename only for clarity

        # Merge all files for this seed into a single rates dict
        rates, eps, _ = merge_txt_files(fps)
        all_rates.append(rates)
        all_episodes.append(eps)
        print(f"    -> {len(rates)} task trovati")

    # ── Step 2: Load reference mapping and fixed task order ────────────────
    ref_mapping = get_reference_mapping(level)    # Composition task → training reference
    fixed_order = (
        get_task_comp_l2_order() if level == "l2" else get_task_comp_l1_order()
    )  # Canonical task list for row ordering

    # ── Step 3: Create Excel workbook and worksheet ────────────────────────
    wb = Workbook()
    ws = wb.active
    ws.title = f"Task Comp {level.upper()}"  # Name the worksheet (e.g., "Task Comp L1")

    # ── Step 4: Write and format the header row ────────────────────────────
    headers = [
        "# Task",
        "Task Command",
        "Reference Task",
        "SR% - Seed 0", "Task Completion - Seed 0",
        "SR% - Seed 1", "Task Completion - Seed 1",
        "SR% - Seed 2", "Task Completion - Seed 2",
        "Mean SR% ± Std",
        "Mean Task Completion",
    ]
    ws.append(headers)

    # Apply bold font and center-aligned text wrapping to all header cells
    for cell in ws[1]:
        cell.font = Font(bold=True)
        cell.alignment = Alignment(horizontal="center", wrap_text=True)

    # ── Step 5: Initialize accumulators for the final summary row ──────────
    # all_seed_rates[k]       : list of per-task success rates for seed k (NaN excluded)
    # all_seed_completions[k] : list of (success_count, episode_count) tuples for seed k
    all_seed_rates = [[], [], []]
    all_seed_completions = [[], [], []]

    # ── Step 6: Process each task in canonical fixed order ─────────────────
    for task_idx, task_cmd in enumerate(fixed_order, start=1):
        # Normalize the canonical task name for dictionary lookups
        tk = normalize_task_key(task_cmd)

        # Look up the training reference task(s) for this composition task
        ref_task = ref_mapping.get(tk, "")  # Empty string if no mapping found

        seed_rates = []         # Success rates (%) for this task, one per seed
        seed_completions = []   # "N/M" completion strings, one per seed

        # ── Extract per-seed data for this task ────────────────────────────
        for seed_idx in range(3):
            # Retrieve rate; float('nan') if this task is absent from this seed's data
            rate = all_rates[seed_idx].get(tk, float("nan"))
            episodes = all_episodes[seed_idx].get(tk, 50)  # Default to 50 episodes

            # Compute integer success count: only if rate is a valid number (not NaN)
            # NaN check: `rate == rate` is False for NaN (IEEE 754 property)
            succ = int(round(rate / 100 * episodes)) if rate == rate else 0

            seed_rates.append(rate)
            seed_completions.append(f"{succ}/{episodes}")

            # Accumulate only valid (non-NaN) rates for the summary row
            if rate == rate:  # True only when rate is not NaN
                all_seed_rates[seed_idx].append(rate)
                all_seed_completions[seed_idx].append((succ, episodes))

        # ── Compute per-task mean ± sample std across seeds ────────────────
        # Filter out NaN values before computing statistics
        valid = [r for r in seed_rates if r == r]

        if valid:
            mean_r = sum(valid) / len(valid)  # Arithmetic mean of valid rates

            # Sample standard deviation (N-1 denominator); 0.0 if only one valid rate
            std_r = (
                math.sqrt(sum((r - mean_r) ** 2 for r in valid) / (len(valid) - 1))
                if len(valid) > 1 else 0.0
            )
            mean_display = f"{mean_r:.1f}% ± {std_r:.1f}%"

            # Average episode count across seeds (should always be 50)
            avg_episodes = int(round(
                sum(all_episodes[seed_idx].get(tk, 50) for seed_idx in range(3)) / 3
            ))
            # Mean task completion: average successes per seed out of avg_episodes
            avg_succ = int(round(mean_r / 100 * avg_episodes))
        else:
            # No valid data for this task across any seed
            mean_display = "N/A"
            avg_episodes = 50
            avg_succ = 0

        # ── Append data row to the worksheet ──────────────────────────────
        ws.append([
            task_idx,            # Column 1: 1-based task index
            task_cmd,            # Column 2: canonical task description (original case)
            ref_task,            # Column 3: training reference task(s)
            # Columns 4-5: Seed 0 success rate and completion
            f"{seed_rates[0]:.1f}%" if seed_rates[0] == seed_rates[0] else "N/A",
            seed_completions[0],
            # Columns 6-7: Seed 1
            f"{seed_rates[1]:.1f}%" if seed_rates[1] == seed_rates[1] else "N/A",
            seed_completions[1],
            # Columns 8-9: Seed 2
            f"{seed_rates[2]:.1f}%" if seed_rates[2] == seed_rates[2] else "N/A",
            seed_completions[2],
            mean_display,                    # Column 10: mean ± std across seeds
            f"{avg_succ}/{avg_episodes}",    # Column 11: mean task completion as N/50
        ])

    # ── Step 7: Build and append the final summary row ─────────────────────
    # This row aggregates performance across all tasks, per seed and globally.
    final_row = ["", "Mean% ± Std%", ""]  # Columns 1-3: index (blank), label, reference (blank)

    # Per-seed aggregate statistics
    for seed_idx in range(3):
        rates = all_seed_rates[seed_idx]  # All valid task rates for this seed

        if rates:
            m = sum(rates) / len(rates)  # Mean success rate across all tasks for this seed

            # Sample std across all task rates for this seed
            s = (
                math.sqrt(sum((r - m) ** 2 for r in rates) / (len(rates) - 1))
                if len(rates) > 1 else 0.0
            )

            # Aggregate completion: total successes and total episodes across all tasks
            ts = sum(c[0] for c in all_seed_completions[seed_idx])  # Total successes
            te = sum(c[1] for c in all_seed_completions[seed_idx])  # Total episodes

            final_row.append(f"{m:.2f}% ± {s:.2f}%")  # "XX.XX% ± YY.YY%"
            final_row.append(f"{ts}/{te}")               # "N/M"
        else:
            final_row.extend(["N/A", "0/0"])  # No data for this seed

    # ── Global mean ± std (standard VLA reporting convention) ─────────────
    # Compute the mean of each seed's mean, then the std across those seed means.
    seed_means = []
    for seed_idx in range(3):
        if all_seed_rates[seed_idx]:
            # Per-seed mean: average success rate across all tasks for seed k
            seed_means.append(
                sum(all_seed_rates[seed_idx]) / len(all_seed_rates[seed_idx])
            )

    if seed_means:
        gm = sum(seed_means) / len(seed_means)  # Global mean (mean of seed means)

        # Sample std across seed means (3 values: one per seed)
        gs = (
            math.sqrt(sum((m - gm) ** 2 for m in seed_means) / (len(seed_means) - 1))
            if len(seed_means) > 1 else 0.0
        )

        # Global completion totals: sum all successes and episodes across all seeds
        gt_s = sum(sum(c[0] for c in all_seed_completions[i]) for i in range(3))
        gt_e = sum(sum(c[1] for c in all_seed_completions[i]) for i in range(3))

        final_row.append(f"{gm:.2f}% ± {gs:.2f}%")  # Global "XX.XX% ± YY.YY%"
        final_row.append(f"{gt_s}/{gt_e}")             # Global "N/M"
    else:
        final_row.extend(["N/A", "0/0"])

    ws.append(final_row)  # Append the completed summary row to the worksheet

    # Apply bold formatting to the final summary row for visual emphasis
    for cell in ws[ws.max_row]:
        cell.font = Font(bold=True)

    # ── Step 8: Auto-fit column widths ────────────────────────────────────
    for col in ws.columns:
        letter = col[0].column_letter  # Get the Excel column letter (A, B, C, ...)

        # Find the length of the longest string value in this column
        max_len = max(
            (len(str(c.value or "")) for c in col),
            default=0  # Default to 0 if the column is empty
        )

        # Set column width: content length + 2 padding, capped at 65 to prevent excessive width
        ws.column_dimensions[letter].width = min(max_len + 2, 65)

    # ── Step 9: Save the workbook to disk ─────────────────────────────────
    wb.save(output_xlsx)
    print(f"\n[OK] Excel salvato: {output_xlsx}")


